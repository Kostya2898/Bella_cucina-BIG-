def register_ai_routes(app, limiter, Dish, sanitize_string):
    """Реєструє /ai_chat маршрут та context_processor."""

    @app.context_processor
    def inject_menu_context():
        try:
            dishes = Dish.query.filter_by(is_active=True).all()
            menu_context = [
                {'name': d.name, 'description': d.description,
                 'price': d.price, 'category': d.category}
                for d in dishes
            ]
        except Exception:
            menu_context = []
        return {'menu_context': menu_context}

    @app.route('/ai_chat', methods=['POST'])
    @limiter.limit("30 per minute")
    def ai_chat():
        data = request.get_json(silent=True) or {}
        user_message = sanitize_string(data.get('message', ''), 500)
        history      = data.get('history', [])[-6:]  # останні 6 повідомлень

        if not user_message:
            return jsonify({'reply': 'Напишіть своє питання!'})

        try:
            dishes = Dish.query.filter_by(is_active=True).all()
        except Exception:
            dishes = []

        reply = local_bot_reply(user_message, dishes, history)

        # Штучна затримка — імітація "друкування"
        delay = min(0.3 + len(reply) * 0.002, 1.6)
        time.sleep(delay)

        return jsonify({'reply': reply})
