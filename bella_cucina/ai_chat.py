# ─────────────────────────────────────────────────────────────────────────────
# Допоміжні функції
# ─────────────────────────────────────────────────────────────────────────────

def _matches(msg: str, keywords: list) -> bool:
    return any(w in msg for w in keywords)

def _dish_lines(dishes, full=False) -> str:
    if full:
        return '\n'.join(
            f'• {d.name} — ${d.price:.2f}\n  {d.description[:80]}...'
            for d in dishes
        )
    return '\n'.join(f'• {d.name} — ${d.price:.2f}' for d in dishes)

def _find_dish_by_name(msg: str, all_dishes: list):
    """Шукає конкретну страву по назві у повідомленні."""
    for d in all_dishes:
        name_lower = d.name.lower()
        # Перевіряємо часткове співпадіння по кожному слову назви
        words = [w for w in name_lower.split() if len(w) > 3]
        if any(w in msg for w in words) or name_lower in msg:
            return d
    return None

def _price_range(all_dishes, min_p=None, max_p=None):
    result = all_dishes
    if min_p is not None:
        result = [d for d in result if d.price >= min_p]
    if max_p is not None:
        result = [d for d in result if d.price <= max_p]
    return sorted(result, key=lambda d: d.price)

def _get_tip() -> str:
    tips = [
        'Що порекомендуєте на вечерю?',
        'Є вегетаріанські страви?',
        'Які десерти маєте?',
        'Скільки коштує найдорожча страва?',
        'Що підійде для романтичного вечора?',
        'Як забронювати столик?',
        'Що швидко приготується?',
        'Порекомендуйте щось легке',
    ]
    return random.choice(tips)


