def local_bot_reply(message: str, dishes: list, history: list = None) -> str:
    msg = message.lower().strip()
    history = history or []

    # Групуємо по категоріях
    by_cat = {}
    for d in dishes:
        by_cat.setdefault(d.category, []).append(d)

    appetizers = by_cat.get('appetizers', [])
    mains      = by_cat.get('main', [])
    desserts   = by_cat.get('desserts', [])
    drinks     = by_cat.get('drinks', [])
    all_dishes = dishes

    # ── 1. Конкретна страва по назві ─────────────────────────────────────────
    found_dish = _find_dish_by_name(msg, all_dishes)
    if found_dish and _matches(msg, ['що таке', 'розкаж', 'опиш', 'що це', 'склад',
                                      'інгредієнт', 'з чого', 'як готу', 'детальн']):
        d = found_dish
        cat_map = {'appetizers': 'Закуска', 'main': 'Основна страва',
                   'desserts': 'Десерт', 'drinks': 'Напій'}
        cat = cat_map.get(d.category, d.category)
        return (f'🍽️ **{d.name}** ({cat})\n\n'
                f'{d.description}\n\n'
                f'💰 Ціна: ${d.price:.2f}\n\n'
                f'Хочете додати до замовлення? Перейдіть до розділу Меню! 👨‍🍳')

    # ── 2. Ціна конкретної страви ─────────────────────────────────────────────
    if found_dish and _matches(msg, ['скільки', 'ціна', 'вартість', 'коштує', 'price']):
        d = found_dish
        return (f'💰 **{d.name}** коштує **${d.price:.2f}**\n\n'
                f'{d.description[:70]}...\n\n'
                f'Смачна страва за гарною ціною! 😋')

    # ── 3. Привітання ─────────────────────────────────────────────────────────
    if _matches(msg, ['привіт', 'hello', 'hi', 'добрий день', 'добрий вечір',
                      'доброго', 'вітаю', 'салют', 'hey', 'хай', 'йо']):
        greetings = [
            'Вітаю! 👋 Я шеф-асистент Bella Cucina — знаю про кожну страву все!\nЩо вас цікавить сьогодні? Можу порекомендувати щось особливе 🍽️',
            'Привіт! 😊 Радий бачити вас у Bella Cucina!\nРозкажіть що любите — і я підберу ідеальну страву для вас 👨‍🍳',
            'Доброго дня! ✨ Ласкаво просимо до Bella Cucina!\nЯ тут щоб допомогти вам зробити найкращий вибір у меню 🍝',
        ]
        return random.choice(greetings)

    # ── 4. Як справи / що нового ──────────────────────────────────────────────
    if _matches(msg, ['як справ', 'як ти', 'що нового', 'як діла', 'що робиш']):
        return ('Дякую, чудово! 😄 Допомагаю гостям обирати найсмачніші страви.\n'
                'А у вас як? Може вже знаєте що хочете замовити, чи допомогти з вибором? 🍽️')

    # ── 5. Загальні рекомендації ──────────────────────────────────────────────
    if _matches(msg, ['рекоменд', 'порад', 'що обрати', 'що замовити',
                      'що взяти', 'найкращ', 'топ', 'хіт', 'спробувати']):
        top_app  = appetizers[0] if appetizers else None
        top_main = mains[0] if mains else None
        top_des  = desserts[0] if desserts else None
        top_drink = drinks[0] if drinks else None
        reply = '🌟 Мій ідеальний сет для вечері у Bella Cucina:\n\n'
        if top_app:   reply += f'🥗 Закуска: **{top_app.name}** — ${top_app.price:.2f}\n'
        if top_main:  reply += f'🍝 Основне: **{top_main.name}** — ${top_main.price:.2f}\n'
        if top_des:   reply += f'🍮 Десерт: **{top_des.name}** — ${top_des.price:.2f}\n'
        if top_drink: reply += f'🍷 Напій: **{top_drink.name}** — ${top_drink.price:.2f}\n'
        reply += '\nКожна страва — окремий витвір мистецтва! 😋'
        return reply

    # ── 6. Популярне / улюблене ───────────────────────────────────────────────
    if _matches(msg, ['популярн', 'часто замовл', 'улюблен', 'фаворит', 'хіт продаж']):
        picks = mains[:2] + desserts[:1]
        lines = _dish_lines(picks)
        return f'🏆 Хіти Bella Cucina — страви які замовляють найчастіше:\n\n{lines}\n\nНаші гості в захваті! Рекомендую спробувати 😍'

    # ── 7. Закуски ────────────────────────────────────────────────────────────
    if _matches(msg, ['закуск', 'аперитив', 'почат', 'стартер', 'перед основн', 'перекус']):
        if not appetizers:
            return 'На жаль, зараз закуски тимчасово недоступні. Але основні страви та десерти чудові! 🍝'
